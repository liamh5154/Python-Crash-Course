"""Ordinal numbers indicate their position in a list, such as 1st or 2nd.
Most ordinal numbers end in 'th' except 1, 2 & 3."""

ordinal_numbers = range(1,10)
for numbers in ordinal_numbers:
	if number == 1:
		print(number + "st")
	elif number == 2:
		print(number + "nd")
	elif number == 3:
		print(number + "rd")
	elif number == 4:
		print(number + "th")
	elif number == 5:
		print(number + "th")
	elif number == 6:
		print(number + "th")
	elif number == 7:
		print(number + "th")
	elif number == 8:
		print(number + "th")
	elif number == 9:
		print(number + "th")
print(ordinal_numbers)	
